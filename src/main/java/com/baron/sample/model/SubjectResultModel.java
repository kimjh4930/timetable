package com.baron.sample.model;

public class SubjectResultModel {
	private String subjectCode;
	private String subjectName;
	private String subjectDept;
	private String subjectDivide;
	private String grade;
	private String credit;
	private String professorName;
	private String classTime;
	private String classroom;

	public String getSubjectCode() {
		return subjectCode;
	}

	public void setSubjectCode(String subjectCode) {
		this.subjectCode = subjectCode;
	}

	public String getSubjectName() {
		return subjectName;
	}

	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}

	public String getSubjectDept() {
		return subjectDept;
	}

	public void setSubjectDept(String subjectDept) {
		this.subjectDept = subjectDept;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}

	public String getCredit() {
		return credit;
	}

	public void setCredit(String credit) {
		this.credit = credit;
	}

	public String getProfessorName() {
		return professorName;
	}

	public void setProfessorName(String professorName) {
		this.professorName = professorName;
	}

	public String getClassTime() {
		return classTime;
	}

	public void setClassTime(String classTime) {
		this.classTime = classTime;
	}

	public String getClassroom() {
		return classroom;
	}

	public void setClassroom(String classroom) {
		this.classroom = classroom;
	}

	public String getSubjectDivide() {
		return subjectDivide;
	}

	public void setSubjectDivide(String subjectDivide) {
		this.subjectDivide = subjectDivide;
	}

}
